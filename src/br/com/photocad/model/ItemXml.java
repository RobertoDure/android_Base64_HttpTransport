package br.com.photocad.model;



public class ItemXml {
	
	private String id;
	private String nome;
	private String link;
	
	//construtor
	public ItemXml() {
	
	}
	
	public String getId() {
		return id;
	}


	public void setId(String id) {
		this.id = id;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	

}
